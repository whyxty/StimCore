"""Pydantic-модели для API. Унифицированный input — все параметры одной формой.

Соответствует п.4(a) — все параметры всех 12 задач в единой форме.
"""
from typing import Literal, Optional, List, Dict, Any
from pydantic import BaseModel, Field


# ───────── Унифицированный ввод ─────────
class WellInputs(BaseModel):
    """Все параметры скважины и обработки для расчёта 12 задач СКО."""

    # ── Идентификация ──
    well_name: str = "Скважина №1"
    field_name: str = "Предкарпатье"

    # ── В.1: продуктивность, выбор скважины (полная информация) ──
    K_f:       float = 16.0      # фактический коэф. продуктивности, т/(сут·МПа)
    K_pot:     float = 51.0      # потенциальный коэф. продуктивности, т/(сут·МПа)
    h_pta:     float = 17.0      # h_пгл — толщина поглощающего пласта, м
    k_ms_lab:  float = 1.20      # лаб. прирост пористости после СКО
    k_mg_lab:  float = 1.15      # лаб. прирост после ГКО
    layers:    List[Dict[str, Any]] = Field(default_factory=lambda: [
        {"interval": "2733-2750", "h_ef": 17,  "porosity": 12.0, "k0": 0.020, "treat": True},
        {"interval": "2750-2762", "h_ef": 12,  "porosity": 9.8,  "k0": 0.010, "treat": True},
        {"interval": "2762-2780", "h_ef": 18,  "porosity": 13.0, "k0": 0.060, "treat": False},
    ])

    # ── В.2: ограниченная информация ──
    Q_f:       float = 86.6      # фактический дебит, м³/сут
    q_inj:     float = 150.0     # приёмистость, м³/сут
    p_pl:      float = 25.0      # пластовое давление, МПа
    H:         float = 2800.0    # глубина, м

    # ── В.3: параметры закачки ──
    well_type: Literal["нефтяная", "водонагнетательная"] = "нефтяная"
    p_opr:     float = 25.0      # давление опрессовки, МПа
    rho_fluid: float = 1070.0    # плотность жидкости (СКР), кг/м³
    q_k:       float = 250.0     # расход СКР, м³/сут
    p_k:       float = 12.0      # давление на устье при КО, МПа
    use_v20:   bool  = False

    # ── В.5: зона растворения ──
    r_c:       float = 0.10      # радиус скважины, м
    h_ef:      float = 78.3      # эфф. толщина, м
    m0:        float = 14.0      # начальная пористость, %
    k_vo:      float = 0.35      # k_в.о — охват по вертикали
    k_uf:      float = 0.28      # k_у.ф — участие пор
    k_v:       float = 0.50      # k_в — вытеснение
    rho_sk:    float = 2700.0    # плотность скелета, кг/м³
    R_ms:      float = 2.0e-5    # средняя растворимость, кг/(м³·экв)
    C0_HCl:    float = 15.0      # концентрация СКР, %
    V_zad:     float = 12.0      # заданный объём СКР, м³
    k_ms_mode: Literal["напрямую", "по Δm_s"] = "напрямую"
    dm_s:      float = 2.8       # Δm_s, % (если режим "по Δm_s")

    # ── В.6: состав породы ──
    C_gl:      float = 6.6       # глинистость, %
    C_k:       float = 3.1       # карбонатность, %
    rho_p:     float = 2250.0    # плотность пористой породы, кг/м³

    # ── В.8: проницаемость ──
    KL:        Literal["KL_1", "KL_2", "KL_3", "KL_4", "KL_5"] = "KL_2"
    k0_use_override: bool = False
    k0_user:   float = 0.044

    # ── В.9: эффективность ──
    r_k:       float = 250.0     # радиус контура питания, м
    T_n:       float = 100.0     # длительность повыш. дебита, сут
    rho_n:     float = 840.0     # плотность нефти, кг/м³
    W_0:       float = 20.0      # обводнённость, %
    C_n:       float = 30000.0   # цена нефти, руб/т
    S_n:       float = 18000.0   # себестоимость, руб/т
    Z_ko:      float = 150000.0  # стоимость СКО, руб
    use_eps:   bool  = True      # учитывать ε_от

    # ── В.10: продавочная и вытесняющая жидкости ──
    d_in_NKT:  float = 0.062     # внутр. диаметр НКТ, м
    d_out_NKT: float = 0.073     # наружн. диаметр НКТ, м
    D_k:       float = 0.150     # внутр. диаметр э/к, м
    H_no:      float = 2820.0    # глубина нижнего отверстия перф., м
    H_vo:      float = 2780.0    # глубина верхнего отверстия перф., м

    # ── В.11: рецептура ──
    N_ko:      int   = 0         # число прошлых КО
    T_pl:      float = 70.0      # пластовая температура, °C
    Fe3:       float = 0.0       # ожид. содержание Fe³⁺, %
    W_vn:      Literal[1, 2, 3] = 2  # тип пластовой воды
    N_as:      float = 1.0       # асфальтены, %
    N_sm:      float = 4.0       # смолы, %
    N_nf:      float = 0.2       # нафтеновые кислоты, %
    HCl:       float = 12.0      # концентрация HCl в КР, %
    HF:        float = 3.0       # концентрация HF (если ГКО), %
    ratio_skr_gkr: str = "1:1"

    # ── В.17: товарные характеристики ──
    tovar_HCl_options: List[int] = Field(default_factory=lambda: [27, 31])
    C_inh:     float = 0.3       # концентрация ингибитора в КР, %
    C_stab:    float = 0.5       # концентрация стабилизатора в КР, %
    C_surf:    float = 0.3       # концентрация ПАВ в КР, %
    C_buf:     float = 0.3       # концентрация ПАВ в буферных, %


# ───────── Результаты ─────────
class CalcResults(BaseModel):
    """Результаты всех 12 задач (произвольная структура — фронт сам разберёт)."""
    inputs:    Dict[str, Any]
    constants: Dict[str, Any]
    tasks:     Dict[str, Any]    # {"v1": {...}, "v2": {...}, ...}
